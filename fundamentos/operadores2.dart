main() {
 
  //Operadores de Atribuição

  int a = 2;
  a += 3; //mesmo que (a = a + 3)
  a -= 3;
  a *= 3;
  a ~/= 2;
  a %=2;

  //Operadores Relacionais; o resultado sempre retornara booleano

  print(3>2);
  print(3>=2);
  print(3<4);
  print(3<=3);
  print(3!=3);
  print(3 == 3);
  print(3 == '3');


  print(a);

}
